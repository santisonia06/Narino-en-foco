function myFunction() {
  var dotsUno = document.getElementById("dots");
  var moreTextUno = document.getElementById("more");
  var btnTextUno = document.getElementById("myBtn");

  if (dotsUno.style.display === "none") {
    dotsUno.style.display = "inline";
    btnTextUno.innerHTML = "Leer mas";
    moreTextUno.style.display = "none";
  } else {
    dotsUno.style.display = "none";
    btnTextUno.innerHTML = "Leer menos";
    moreTextUno.style.display = "inline";
  }
}

